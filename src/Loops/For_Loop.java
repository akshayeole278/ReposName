package Loops;

public class For_Loop {

	public static void main(String[] args) {
		
		for(int j=1;j<=10;j++)
		{
			System.out.println(j);
		}
		
		System.out.println("_________________________");

		for(int k=1;k<=10;k+=2)
		{
			System.out.println(k);
		}
		
		System.out.println("_________________________");
		
		for(int l=20;l>=10;l--)
		{
			System.out.println(l);
		}
		
		System.out.println("_________________________");
	
	}
	

}
