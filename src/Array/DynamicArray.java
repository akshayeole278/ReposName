package Array;

public class DynamicArray {

	public static void main(String[] args) {
		
		int s[]= {10,20,30,40,50};
		
		int sum=0;
		
		for(int i:s)
		{
			System.out.println(i);
			sum=sum+i;
		}
		
		System.out.println("Sum is " +sum);
		System.out.println("____________________________");
		
		
	}

}
