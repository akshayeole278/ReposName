package Condition_statements;

public class Nested_IfCondition {

	public static void main(String[] args) 
	{
		int holiday=1;
		
		if (holiday==1)
		{
			System.out.println("Its a Saturday");
		}
		else if (holiday==2)
		{
			System.out.println("Its a Sunday");
		}
		else
		{
			System.out.println("Its a Working Day");
		}
	}
	

}
